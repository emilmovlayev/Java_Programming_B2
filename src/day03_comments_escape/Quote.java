package day03_comments_escape;

public class Quote {
    public static void main(String[] args) {
          // Escape quotes (backward slash)

        System.out.println("I like Java programming ");
        System.out.println("I like \"Java\" programming ");
        System.out.println("I like \\Java\\ programming ");
        System.out.println("abc\\def");
        System.out.println("abc\\\\def");
    }
}
