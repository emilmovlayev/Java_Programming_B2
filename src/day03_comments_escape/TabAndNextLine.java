package day03_comments_escape;

public class TabAndNextLine {
    public static void main(String[] args) {
        System.out.println("This is not tabbed");
        System.out.println("\tThis is not tabbed"); // this is tabbed 1 time
        System.out.println("    This is not tabbed"); // 4 space equal 1 tab
        System.out.println("\t\tThis is tabbed 2 times");

        System.out.println("Go to the store\nGrab milk and bread\n");
        // kodlari organize etmek : ctrl+alt+l
        // highlight everything :   ctrl+a
        // put everything comment : ctrl+/

    }
}
